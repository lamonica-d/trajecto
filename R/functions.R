f_derivee <- function(a,b,c,x, mu, sd){ ((b*sd + 2*c*(x-mu))*exp(a + ((x-mu)*(b*sd + c*(x-mu)))/sd^2))/(sd^2*(exp(a+((x-mu)*(b*sd-c*mu+c*x))/sd^2)+1)^2)}

